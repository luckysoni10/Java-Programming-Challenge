import java.util.*;
public class Binary_Operators {
    public static void main(String[] args) {
        //Binary AND
        System.out.print("\nBinary AND 5 & 6 = ");
        System.out.print(5 & 6);

        //Binary OR
        System.out.print("\nBinary OR 5 | 6 = ");
        System.out.print(5 | 6);

        //Binary XOR
        System.out.print("\nBinary XOR 5 ^ 6 = ");
        System.out.print(5 ^ 6);

        //Binary One's Compliment
        System.out.print("\nBinary 1's Compliment ~5 = ");
        System.out.print(~5);

        //Binary Left Shift
        System.out.print("\nBinary Left Shift 5 << 2 = ");
        System.out.print(5 << 2);

        //Binary Right Shift
        System.out.print("\nBinary Right Shift 5 >> 2 = ");
        System.out.print(5 >> 2);
  
    }
}

// Output- 
// Binary AND 5 & 6 = 4
// Binary OR 5 | 6 = 7
// Binary XOR 5 ^ 6 = 3
// Binary 1's Compliment ~5 = -6
// Binary Left Shift 5 << 2 = 20
// Binary Right Shift 5 >> 2 = 1