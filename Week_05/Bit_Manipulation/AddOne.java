//Add 1 to an integer
public class AddOne {
    public static void main(String[] args) {
        int x = 6;
        System.out.println(x+" + "+1+" is "+-~x);
        x = -4;
        System.out.println(x+" + "+1+" is "+-~x);
        x = 0;
        System.out.println(x+" + "+1+" is "+-~x);
    }
}
// Output-7
// 6 + 1 is 7
// -4 + 1 is -3
// 0 + 1 is 1